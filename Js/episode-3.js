// Programme qui demande à l'utilisateur deux nombres et affiche leur somme en alerte.
const num1 = parseFloat(prompt("Entrez le premier nombre :"));
const num2 = parseFloat(prompt("Entrez le deuxième nombre :"));
const sum = num1 + num2;
alert(`La somme de ${num1} et ${num2} est ${sum}`);

